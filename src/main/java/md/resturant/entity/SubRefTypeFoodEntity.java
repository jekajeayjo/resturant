package md.resturant.entity;

import javax.persistence.*;

@Entity
@Table(name = "sub_ref_type_food", schema = "public", catalog = "restaurant_db")
public class SubRefTypeFoodEntity {
    private Long id;
    private String code;
    private String name;
    private RefTypeFoodEntity fkRefTypeFood;

    @Id
    @Column(name = "id")
    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Basic
    @Column(name = "code")
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    @Basic
    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @JoinColumn(name = "fk_ref_type_food")
    @ManyToOne(fetch = FetchType.LAZY)
    public RefTypeFoodEntity getFkRefTypeFood() {
        return fkRefTypeFood;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        SubRefTypeFoodEntity that = (SubRefTypeFoodEntity) o;

        if (id != that.id) return false;
        if (code != null ? !code.equals(that.code) : that.code != null) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = (int) (id ^ (id >>> 32));
        result = 31 * result + (code != null ? code.hashCode() : 0);
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}
